	inline bool Decompress(uint8_t* destBuffer, size_t destBufferSize)
	{
		if (destBufferSize < Internal::MAIN_CPP_ORIGINAL_SIZE) return false;
		unsigned long destLen = destBufferSize;
		int status = mz_uncompress(
			destBuffer, 
			&destLen, 
			Internal::MAIN_CPP_DATA, 
			Internal::MAIN_CPP_COMPRESSED_SIZE
		);
		return status == MZ_OK;
	}

	inline bool Decompress(std::ofstream& file)
	{
		if (!file.is_open()) return false;
		mz_stream stream = {0};
		stream.next_in = Internal::MAIN_CPP_DATA;
		stream.avail_in = Internal::MAIN_CPP_COMPRESSED_SIZE;
		if (mz_inflateInit(&stream) != MZ_OK) return false;

		constexpr size_t CHUNK_SIZE = 32768; // 32 KB fixed stack buffer
		uint8_t outBuffer[CHUNK_SIZE];
		int z_status;

		do {
			stream.next_out = outBuffer;
			stream.avail_out = CHUNK_SIZE;
			z_status = mz_inflate(&stream, MZ_NO_FLUSH);

			size_t bytesDecompressed = CHUNK_SIZE - stream.avail_out;
			if (bytesDecompressed > 0) {
				file.write(reinterpret_cast<const char*>(outBuffer), bytesDecompressed);
			}
		} while (z_status == MZ_OK);

		mz_inflateEnd(&stream);
		return z_status == MZ_STREAM_END;
	}

	inline bool Decompress(const std::filesystem::path& outputPath)
	{
		std::ofstream file(outputPath, std::ios::binary);
		return Decompress(file);
	}
